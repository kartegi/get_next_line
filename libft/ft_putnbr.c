#include "libft.h"

static	void	ft_print(int n)
{
	if (n > 0)
	{
		ft_print(n / 10);
		ft_putchar(n % 10 + '0');
	}
}

void			ft_putnbr(int n)
{
	int		i;
	char	c;

	i = 0;
	c = '\0';
	if (n == -2147483648)
		ft_putstr("-2147483648");
	if (n < 0 && n != -2147483648)
	{
		n = n * -1;
		c = '-';
	}
	if (c == '-')
	{
		ft_putchar('-');
		c = '\0';
	}
	if (n == 0)
		ft_putchar('0');
	if (n > 0)
		ft_print(n);
}
